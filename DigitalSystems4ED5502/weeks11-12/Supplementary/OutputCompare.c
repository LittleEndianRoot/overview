void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  static uint32_t captures[2];
  static int edge_count;   /* Need to be static or global (if used in while(1) */
  static int capture_completed = 0;  /* Statics initialised to 0 anyway */

  uint32_t capture_difference;

  if (edge_count == 0)
  {
	  captures[edge_count] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
	  edge_count++;
  }
  else
  {
	  captures[edge_count] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
	  edge_count = 0;
	  capture_completed = 1;
  }

  if (capture_completed)
  {

	  if (captures[1] > captures[0])
		  capture_difference = captures[1] - captures[0];
	  else
		  capture_difference = ((0xffffffff - captures[0]) + captures[1] + 1);

	  capture_completed = 0;
	  time_period = capture_difference/1;

	  if (time_period > 200)
		  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_SET);
	  else
		  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);
  }
}


